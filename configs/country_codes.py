def get_country_code(country: str, default: str = 'US') -> str:
    """
    Convert country name to ISO 2-letter country code.
    
    Args:
        country: Country name or code
        default: Default country code to return if no match found (defaults to 'US')
    
    Returns:
        Two-letter country code
    
    Examples:
        >>> get_country_code('United States')
        'US'
        >>> get_country_code('Hong Kong SAR')
        'HK'
        >>> get_country_code('DEUTSCHLAND')
        'DE'
    """
    if not country:
        return default
        
    # Clean the input
    cleaned = str(country).strip().upper()
    
    # If it's already a valid 2-letter code, return it
    if len(cleaned) == 2:
        return cleaned
        
    # Look up in mapping
    return COUNTRY_CODES.get(cleaned, default)

def is_valid_country_code(code: str) -> bool:
    """
    Check if a given code is a valid 2-letter country code in our mapping.
    
    Args:
        code: Two-letter country code to validate
    
    Returns:
        bool: True if valid, False otherwise
        
    Example:
        >>> is_valid_country_code('US')
        True
        >>> is_valid_country_code('XX')
        False
    """
    return code in set(COUNTRY_CODES.values())

def get_all_valid_codes() -> set:
    """
    Get all valid country codes in the system.
    
    Returns:
        set: Set of all valid 2-letter country codes
    """
    return set(COUNTRY_CODES.values())